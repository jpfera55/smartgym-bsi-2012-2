    private void disregardButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_disregardButtonActionPerformed
        // TODO add your handling code here:
        Payment payment = (Payment) inadiplentTable.getSelectRow();

        if (payment == null) {
            JOptionPane.showMessageDialog(this, "Nenhuma linha selecionada");
            return;
        }
        int resp = JOptionPane.showConfirmDialog(this, "Deseja desconciderar o pagamento?", "Confirme", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION) {
            disregard(payment);
        }
    }//GEN-LAST:event_disregardButtonActionPerformed

