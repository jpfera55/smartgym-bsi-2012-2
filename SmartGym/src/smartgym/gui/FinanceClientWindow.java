    public void restart() {
        if (client != null) {
            inadiplentTable.setObjectList(paymentController.findPaymentInadiplentByClient(client));
            pedenceTable.setObjectList(paymentController.findPaymentPendenceByClient(client));
        } else {
            JOptionPane.showMessageDialog(this, "Não há client disponivel para esta janela");
            this.dispose();
        }

    }

    public void setClient(Client client) {
        this.client = client;
        restart();
    }

